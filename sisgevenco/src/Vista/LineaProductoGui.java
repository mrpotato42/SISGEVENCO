       public void editar(){// metodo editar que hace unas instrucciones para editar un registro
        LineaProducto objL;// referenciamos el objeto ciudad
        int idLinea=Integer.valueOf(txtIdLinea.getText());//almacenamos en esta variable lo que hay en el txtfield oculto que tiene el idBario
        String descripcion=txtDescp.getText();//almacenamos en esta variable lo que hay en el txtfield nombre
       
        objL=new LineaProducto(idLinea, descripcion);//instanciamos el objeto y le pasamos los valores de las variables que esta arriba
      
       
            
        if(objCtrlLp.modificar(objL)){// validamos que se cumpla todo la modificacion recordemos que el metodo modificar arroja true o false
            JOptionPane.showMessageDialog(null,"La Linea del producto se Edito con Exito!");
            //actualizamos el table
            borrarJtable();
            traerDatosTable();
            //limpiamos los campos textfield
            limpiar();
            txtDescp.setEnabled(false);
            btnGuardar.setEnabled(false);
            btnEliminar.setEnabled(false);
            btnEditar.setEnabled(false);  
        }else{
            JOptionPane.showMessageDialog(null,"La Linea del producto No se Edito!");
        }   
        
    }
       
