    private void tblResolucionFacturaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblResolucionFacturaMouseClicked
        
        int filasele = tblResolucionFactura.getSelectedRow();
        
        txtIdResolucion.setText(tblResolucionFactura.getValueAt(filasele, 0).toString());
        txtNroResolucion.setText(tblResolucionFactura.getValueAt(filasele, 1).toString());        
        String fecha1 = tblResolucionFactura.getValueAt(filasele, 2).toString();
        String fecha2 = tblResolucionFactura.getValueAt(filasele, 3).toString();
        String fecha3 = tblResolucionFactura.getValueAt(filasele, 4).toString();

        SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
        Date fechaTxt1;
        Date fechaTxt2;
        Date fechaTxt3;

        try {
            fechaTxt1 = formatoFecha.parse(fecha1);
            fechaTxt2 = formatoFecha.parse(fecha2);
            fechaTxt3 = formatoFecha.parse(fecha3);

            jdcFecha.setDate(fechaTxt1);
            jdcFechaInicio.setDate(fechaTxt2);
            jdcFechaFin.setDate(fechaTxt3);
            
            txtNroResolucion.setEnabled(true);
            jdcFecha.setEnabled(true);
            jdcFechaInicio.setEnabled(true);
            jdcFechaFin.setEnabled(true);
            btnAgregar.setEnabled(false);
            btnEliminar.setEnabled(true);
            btnEditar.setEnabled(true);

        } catch (ParseException ex) {
            Logger.getLogger(ResolucionFacturaGUI.class.getName()).log(Level.SEVERE, null, ex);
        }

        //filas = filasele;
    }//GEN-LAST:event_tblResolucionFacturaMouseClicked
    
