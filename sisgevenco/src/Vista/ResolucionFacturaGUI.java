    public void consultarDatosTabla() {
        modelo=(DefaultTableModel)tblResolucionFactura.getModel();//nos arroja el modelo es decir el jtable
        elqueOrdena=new TableRowSorter<TableModel>(modelo);//ordena por columnas descendiente o ascendente lo que hay en el jtable
        tblResolucionFactura.setRowSorter(elqueOrdena);//con esto se puede ordenar por col el jtable de manera asc o desc  
        
        CtrlResolucionFactura objCtrResoFac = new CtrlResolucionFactura();
        ArrayList<ResolucionFactura> listado = new ArrayList();
        listado = objCtrResoFac.listadoResolucionFactura();
        
        Object []col=new Object[modelo.getColumnCount()];//creo un obj tipo Object del tamaño de las columnas de mi Table

        for (int i = 0; i < listado.size(); i++) {
                col[0]=listado.get(i).getIdResolucionFactura();
                col[1]=listado.get(i).getNroResolucionFactura();
                col[2]=listado.get(i).getFecha();
                col[3]=listado.get(i).getFechaInicio();
                col[4]=listado.get(i).getFechaFin();
                modelo.addRow(col);//añado el object a la fila
        }
    }

