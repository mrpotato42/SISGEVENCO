    public void agregar() {

        CtrlResolucionFactura objCtrResoFac = new CtrlResolucionFactura();
        ResolucionFactura objMdlResoFac = new ResolucionFactura();
        
        
        Date date1 = jdcFecha.getDate();
        Date date2 = jdcFechaInicio.getDate();
        Date date3 = jdcFechaFin.getDate();

        if (date1 == null || date2 == null || date3 == null) {
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        } else {

            long d1 = date1.getTime();
            java.sql.Date fecha1 = new java.sql.Date(d1);

            long d2 = date2.getTime();
            java.sql.Date fecha2 = new java.sql.Date(d2);

            long d3 = date3.getTime();
            java.sql.Date fecha3 = new java.sql.Date(d3);
            
            objMdlResoFac.setNroResolucionFactura(Integer.valueOf(txtNroResolucion.getText()));            
            objMdlResoFac.setFecha(fecha1);
            objMdlResoFac.setFechaInicio(fecha2);
            objMdlResoFac.setFechaFin(fecha3);

            if (objCtrResoFac.agregar(objMdlResoFac)) {
                JOptionPane.showMessageDialog(null, "Sus datos fueron agregados exitosamente");
                borrarJtable();
                limpiar();
                consultarDatosTabla();
                txtNroResolucion.setEnabled(false);
                jdcFecha.setEnabled(false);
                jdcFechaInicio.setEnabled(false);
                jdcFechaFin.setEnabled(false);
                btnAgregar.setEnabled(false);
                btnEliminar.setEnabled(false);
                btnEditar.setEnabled(false);

            } else {
                JOptionPane.showMessageDialog(null, "Sus datos no fueron agregados");
            }
        }

    }
    
