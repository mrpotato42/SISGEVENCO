    public void buscar(int idProveedores){
        ArrayList<Proveedor> listado=new ArrayList();
        listado=objCtrlProve.traerUnProveedor(idProveedores);
        int tam=listado.size();
        
        for(int index=0;index<tam;index++){
            txtNombre.setText(listado.get(index).getNombre());
            txtNumeroIdentidad.setText(String.valueOf(listado.get(index).getNumeroIdentidad()));                   
            posicioneCBCiudad(listado.get(index).getNomCiudad());
            txtDireccion.setText(listado.get(index).getDireccion());
            txtTelefono.setText(listado.get(index).getTelefono());
            txtEmail.setText(listado.get(index).getEmail());
            posicioneCBEstado(listado.get(index).getEstado());
            
        }
    }
    
 
    public void posicioneCBCiudad(String ciudad){
        int n= cbxCiudad.getItemCount();
        String desc="";
        for(int i=0;i<n;i++){
            desc=cbxCiudad.getItemAt(i).toString();                      
            if(ciudad.compareTo(desc)==0){
                cbxCiudad.setSelectedIndex(i);
                break;
            }
        }
    }
    
    public void posicioneCBEstado(String estado){
        int n= cbxEstado.getItemCount();
        String desc="";
        for(int i=0;i<n;i++){
            desc=cbxEstado.getItemAt(i).toString();                      
            if(estado.compareTo(desc)==0){
                cbxEstado.setSelectedIndex(i);
                break;
            }
        }
    }
    
