    public void ActualizarTablaEnCliGui(){
        modelo=(DefaultTableModel)ProveedorGui.tblProveedores.getModel();
        elqueOrdena=new TableRowSorter<TableModel>(modelo);
        ProveedorGui.tblProveedores.setRowSorter(elqueOrdena);//con esto se puede ordenar por col el jtable de manera asc o desc
        CtrlProveedor objCtrlProve=new CtrlProveedor();
                
        ArrayList<Proveedor> listado=new ArrayList();
        listado=objCtrlProve.traerTodos();
        Object []col=new Object[modelo.getColumnCount()];//creo un obj tipo Object del tamaño de las columnas de mi Table
                
       for(int index=0;index<listado.size();index++){//recorro el arrayList
            col[0]=listado.get(index).getIdProveedor();
            col[1]=listado.get(index).getNombre();
            col[2]=listado.get(index).getNumeroIdentidad();
            col[3]=listado.get(index).getNomCiudad();
            col[4]=listado.get(index).getDireccion();            
            col[5]=listado.get(index).getTelefono();
            col[6]=listado.get(index).getEmail();
            col[7]=listado.get(index).getEstado();
            modelo.addRow(col);//añado el object a la fila
       }
       ProveedorGui.tblProveedores.setModel(modelo);
    }
    
     private void borrarJtable(){
        modelo=(DefaultTableModel)ProveedorGui.tblProveedores.getModel();
        for (int i = ProveedorGui.tblProveedores.getRowCount() -1; i >= 0; i--){ 
                modelo.removeRow(i); 
        }      
    }
    
