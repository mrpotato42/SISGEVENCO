    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
            String nombre=txtNombre.getText().trim();                      
            String numeroIdentidad=txtNumeroIdentidad.getText().trim(); 
            String direccion=txtDireccion.getText().trim();
           //confirma si ya existe ese numero identidad
        if(accionFormProveGui.compareTo("Nuevo")==0){
            
            if(nombre.length()==0){//NOMBRE
                JOptionPane.showMessageDialog(null,"Digite el nombre del proveedor","Error",JOptionPane.ERROR_MESSAGE); 
                txtNombre.grabFocus();
                
            }else if(numeroIdentidad.length()==0){//NUMEROIDENTIDAD
                JOptionPane.showMessageDialog(null,"Digite el numero de Identidad","Error",JOptionPane.ERROR_MESSAGE);
                txtNumeroIdentidad.grabFocus();
                
            }else if(objCtrlProve.existeNumeroID(txtNumeroIdentidad.getText())==1){            
                JOptionPane.showMessageDialog(null,"Digita otro numero de identidad, ya existe un proveedor con ese numero!","Error",JOptionPane.ERROR_MESSAGE); 
                txtNumeroIdentidad.grabFocus();
                
            }else if(direccion.length()==0){            
                JOptionPane.showMessageDialog(null,"Digite la direccion","Error",JOptionPane.ERROR_MESSAGE); 
                txtDireccion.grabFocus();
                
            }else if(txtEmail.getText().length()!=0 && objCtrlProve.ValidarEmail(txtEmail.getText())==false){            
                JOptionPane.showMessageDialog(null,"Digite un correo valido\nEjemplo: (micorreo@dominio.com)","Error",JOptionPane.ERROR_MESSAGE); 
                txtEmail.grabFocus();
                
            }else{
                guardar();
                limpiar();
                ProveedorGui.btnEditarProvee.setEnabled(false);
//                ProveedorGui.btnEliminarProvee.setEnabled(false);
                this.dispose();
            }
        }else if(accionFormProveGui.compareTo("Editar")==0){
                if(nombre.length()==0){//NOMBRE
                JOptionPane.showMessageDialog(null,"Digite el nombre del proveedor","Error",JOptionPane.ERROR_MESSAGE); 
                txtNombre.grabFocus();
                
            }else if(numeroIdentidad.length()==0){//NUMEROIDENTIDAD
                JOptionPane.showMessageDialog(null,"Digite el numero de Identidad","Error",JOptionPane.ERROR_MESSAGE);
                txtNumeroIdentidad.grabFocus();
                
            }else if(objCtrlProve.existeNumeroIDEditar(txtNumeroIdentidad.getText(),
                    ProveedorGui.idProveedores)!=0){            
                JOptionPane.showMessageDialog(null,"Digita otro numero de identidad, ya existe un proveedor con ese numero!","Error",JOptionPane.ERROR_MESSAGE); 
                txtNumeroIdentidad.grabFocus();
                
            }else if(direccion.length()==0){            
                JOptionPane.showMessageDialog(null,"Digite la direccion"); 
                txtDireccion.grabFocus();
                
            }else if(txtEmail.getText().length()!=0 && objCtrlProve.ValidarEmail(txtEmail.getText())==false){            
                JOptionPane.showMessageDialog(null,"Digite un correo valido\nEjemplo: (micorreo@dominio.com)","Error",JOptionPane.ERROR_MESSAGE); 
                txtEmail.grabFocus();
                
            }else{
                
                editar(ProveedorGui.idProveedores);
                ProveedorGui.btnEditarProvee.setEnabled(false);
//                ProveedorGui.btnEliminarProvee.setEnabled(false);
                this.dispose();
            }  
        }
    }//GEN-LAST:event_btnGuardarActionPerformed
    
    public void editar(int idProveedores){
        
        Proveedor objProve=new Proveedor();
                    
            //DATOS DE PROVEEDOR
            objProve.setIdProveedor(idProveedores);
            objProve.setNombre(txtNombre.getText());
            objProve.setNumeroIdentidad(txtNumeroIdentidad.getText());           
            objProve.setCodigoCiudad(Integer.valueOf(txtCodiCiudad.getText()));
            objProve.setDireccion(txtDireccion.getText());
            objProve.setTelefono(txtTelefono.getText());
            objProve.setEmail(txtEmail.getText());
            objProve.setEstado(cbxEstado.getSelectedItem().toString());            
        if(objCtrlProve.modificar(objProve)){
            JOptionPane.showMessageDialog(null,"Proveeedor Editado con Exito!");
            borrarJtable();
            ActualizarTablaEnCliGui();
        }else{
            JOptionPane.showMessageDialog(null,"Error al Editar!!!");
        }
        
    }
    
    
