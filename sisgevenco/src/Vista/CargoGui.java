       public void editar(){// metodo editar que hace unas instrucciones para editar un registro
        Cargo objD;// referenciamos el objeto ciudad
        int idCargo=Integer.valueOf(txtIdCargo.getText());//almacenamos en esta variable lo que hay en el txtfield oculto que tiene el idBario
        String nombre=txtNombre.getText();//almacenamos en esta variable lo que hay en el txtfield nombre
        String descripcion=String.valueOf(txtAreaDescripcion.getText());//almacenamos en esta variable lo que hay en el txtfield comuna
        
        objD=new Cargo(idCargo, nombre, descripcion);//instanciamos el objeto y le pasamos los valores de las variables que esta arriba
        
        if(objCtrlD.modificar(objD)){// validamos que se cumpla todo la modificacion recordemos que el metodo modificar arroja true o false
            JOptionPane.showMessageDialog(null,"El Cargo se Edito con Exito!");
            //actualizamos el table
            borrarJtable();
            traerDatosTable();
            //limpiamos los campos textfield
            limpiar();
            
            btnGuardar.setEnabled(false);
            btnEliminar.setEnabled(false);
            btnEditar.setEnabled(false);
            txtNombre.setEnabled(false);
            txtAreaDescripcion.setEnabled(false);
        }else{
            JOptionPane.showMessageDialog(null,"El Cargo No se Edito!");
        }   
        
    }
       
