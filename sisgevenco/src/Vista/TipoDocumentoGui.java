    private void borrarJtable(){// este metodo limpia todo el jtable
        for (int i = tblTipoDocumento.getRowCount() -1; i >= 0; i--){ 
                modelo.removeRow(i); 
        }      
    }
    
    public void traerDatosTable(){//como su nombre lo indica va a traer todos los datos en el jtable desde la base de datos
        modelo=(DefaultTableModel)tblTipoDocumento.getModel();//nos arroja el modelo es decir el jtable
        elqueOrdena=new TableRowSorter<TableModel>(modelo);//ordena por columnas descendiente o ascendente lo que hay en el jtable
        tblTipoDocumento.setRowSorter(elqueOrdena);//con esto se puede ordenar por col el jtable de manera asc o desc
                       
        ArrayList<TipoDocumento> listado=new ArrayList();//declaramos e instanciamos un array list de tipo Barrio llamado listado
        listado=objCtrlTD.traer();//almacenamos en listado lo que arroja el metodo listadoBarrio() que esta en la clase CtrlBarrio
        //recordemos que ese metodo lo que arroja es un arraylist(arrayList es como un vector mejorado mil veces) INVESTIGUEN MEJOR!
        Object []col=new Object[modelo.getColumnCount()];//creo un obj tipo Object del tamaño de las columnas de mi Table
               
       for(int index=0;index<listado.size();index++){//recorro el arrayList
                col[0]=listado.get(index).getCodigoDocumento();
                col[1]=listado.get(index).getDescripcion();
                                       
                modelo.addRow(col);//añado el object a la fila
       }
     
    }
    
    public void EsconderColumnaCodigo(){
        //metodo para esconder la columna que contiene el codigo de los Barios
        tblTipoDocumento.getColumnModel().getColumn(0).setMaxWidth(0);
        tblTipoDocumento.getColumnModel().getColumn(0).setMinWidth(0);
        tblTipoDocumento.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
        tblTipoDocumento.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);
    }
    
