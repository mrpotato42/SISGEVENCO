       public void editar(){// metodo editar que hace unas instrucciones para editar un registro
        TipoDocumento objTD;// referenciamos el objeto Barrio
        int idtipoidentidad=Integer.valueOf(txtIdTipoDocumento.getText());//almacenamos en esta variable lo que hay en el txtfield oculto que tiene el idBario
        String descripcion=txtDescripcion.getText();//almacenamos en esta variable lo que hay en el txtfield nombre
        //almacenamos en esta variable lo que hay en el txtfield comuna
        
        objTD=new TipoDocumento(idtipoidentidad, descripcion);//instanciamos el objeto y le pasamos los valores de las variables que esta arriba
        
        if(objCtrlTD.modificar(objTD)){// validamos que se cumpla todo la modificacion recordemos que el metodo modificar arroja true o false
            JOptionPane.showMessageDialog(null,"El tipo de Documento se Edito con Exito!");
            //actualizamos el table
            borrarJtable();
            traerDatosTable();
            //limpiamos los campos textfield
            limpiar();
            txtDescripcion.setEnabled(false);
            btnGuardar.setEnabled(false);
            btnEliminar.setEnabled(false);
            btnEditar.setEnabled(false);  
        }else{
            JOptionPane.showMessageDialog(null,"El tipo de Documento No se Edito!");
        }   
        
    }
       
