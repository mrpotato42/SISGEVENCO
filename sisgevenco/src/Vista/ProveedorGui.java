    public void buscarEnTiempoReal(String texto){
        borrarJtable();//borro lo que hay en el JTABLE
        modelo=(DefaultTableModel)tblProveedores.getModel();
        elqueOrdena=new TableRowSorter<TableModel>(modelo);
        tblProveedores.setRowSorter(elqueOrdena);//con esto se puede ordenar por col el jtable de manera asc o desc
                        
        ArrayList<Proveedor> listado=new ArrayList();
        listado=objCtrlProve.BusquedaTiempoReal(texto);
        Object []col=new Object[modelo.getColumnCount()];//creo un obj tipo Object del tama√±o de las columnas de mi Table
        for(int index=0;index<listado.size();index++){//recorro el arrayList
            col[0]=listado.get(index).getIdProveedor();
            col[1]=listado.get(index).getNombre();
            col[2]=listado.get(index).getNumeroIdentidad();
            col[3]=listado.get(index).getNomCiudad();
            col[4]=listado.get(index).getDireccion();            
            col[5]=listado.get(index).getTelefono();
            col[6]=listado.get(index).getEmail(); 
            col[7]=listado.get(index).getEstado();            
            modelo.addRow(col);
        } 
       
      tblProveedores.setModel(modelo);
         
     }
     
    private void borrarJtable(){
        for (int i = tblProveedores.getRowCount() -1; i >= 0; i--){ 
                modelo.removeRow(i); 
        }      
    }
